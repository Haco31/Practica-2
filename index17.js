/* Desestructuración
La desestructuración permite extraer valores de arrays 
o propiedades de objetos.
*/

let [a, b] = [1, 2];
console.log(a); // 1
console.log(b); // 2

let { nombre, edad } = { nombre: "Carlos", edad: 30 };
console.log(nombre); // Carlos
console.log(edad);   // 30

// -------------------------------------------------- //

