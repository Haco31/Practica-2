/* Operadores de JS como:
- Aritméticos: +, -, *, /, %, ++, --
- Comparación: 
    == : Igualdad, 
    === : Igualdad estricata (Valor y tipo de dato), 
    != : Desiguladad (sin importar el tipo de dato), 
    !== : Desigualdad estricta (compara valor y tipo de dato), 
    > : Mayor que, 
    < : Menor que, 
    >= : Mayor igual que, 
    <= : Menor igual que.
- lógicos: 
    && : AND logico - True si ambas expresiones son verdaderas, 
    || : OR logico - True si al menos una es verdadera, 
    ! : Not logico - niega una expresión booleana.
*/

let a = 10;
let b = 5;

console.log(a + b);
console.log(a > b);
console.log(a===5 && b===10);

